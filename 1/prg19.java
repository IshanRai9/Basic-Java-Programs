
class prg19
{
    public static void main(char xyz)
    {
        if(xyz>='A'&&xyz<='Z')
        System.out.println("upper case alphabet");
        else if(xyz>='a'&&xyz<='z')
        System.out.println("lower case alphabet");
       else if(xyz>='0'&&xyz<='9')
        System.out.println("digit");
        else
        System.out.println("special characters");
    }
}

        